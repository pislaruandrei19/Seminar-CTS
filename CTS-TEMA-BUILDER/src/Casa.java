
class Casa {
    private String fundatie;
    private String pereti;
    private String acoperis;


    public void setFundatie(String fundatie) {
        this.fundatie = fundatie;
    }

    public String getFundatie() {
        return fundatie;
    }


    public void setPereti(String pereti) {
        this.pereti = pereti;
    }

    public String getPereti() {
        return pereti;
    }


    public void setAcoperis(String acoperis) {
        this.acoperis = acoperis;
    }

    public String getAcoperis() {
        return acoperis;
    }
}